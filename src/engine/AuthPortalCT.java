	private String parseAuthenPage(String output) {
		Matcher inputMatcher = loginUrlPattern.matcher(output);
		if (inputMatcher.find()) {
			StringBuffer action = new StringBuffer();
			action.append(inputMatcher.group(1));
			if (action.toString().contains("?")) {
				action.append("&");
			} else {
				action.append("?");
			}
			action.append("button=Login&FNAME=0&OriginatingServer=http://www.akazam.com");
			String province = getAccountLocation(user);
			action.append("&UserName=").append(user).append("@").append(province);
			action.append("&province=").append(province);
			action.append("&Password=").append(password);
			if (user.toUpperCase().startsWith("CH")) {
				action.append("&UserType=1&isChCardUser=true&isWCardUser=false");
			} else if (user.toUpperCase().startsWith("W")) {
				action.append("&UserType=1&isChCardUser=false&isWCardUser=true");
			} else {
				if (Pattern.compile("(18[09]|13[35])\\d{8}").matcher(user).find()) {
					action.append("&UserType=2");
				} else {
					action.append("&UserType=3");
				}
			}
			return action.toString();
		}
		return null; 
	}
	
	private String getAccountLocation(String user) {
		Iterator<ArrayList<String>> it = provs.iterator();
		while (it.hasNext()) {
			Iterator<String> item = it.next().iterator();
			if (item.hasNext()) {
				String province = item.next();
				while (item.hasNext()) {
					Pattern pattern = Pattern.compile(item.next());
					if (pattern.matcher(user).find()) {
						return province;
					}
				}
			}
		}
		return "wlan.zx.chntel.com";
	}

	private String parseLogoutURL(String output) {
		Matcher inputMatcher = logoutUrlPattern.matcher(output);
		if (inputMatcher.find()) {
			return inputMatcher.group(1);
		}
		return null; 
	}
	
	public int login(String user, String password) {
		this.user = user;
		this.password = password;
		Logger.getInstance().writeLog("CT Account Location: " + getAccountLocation(user));
		try {			
			HttpResponse response = httpClient.execute(new HttpGet(LOGIN_TEST_URL));
			String output = EntityUtils.toString(response.getEntity(), "GBK");
			Logger.getInstance().writeLog("Http Request:\n" + LOGIN_TEST_URL);
			Logger.getInstance().writeLog("HTTP Response:\n" + output);
			
			if (output.contains(LOGIN_TEST_SIGNATURE)) {
				Logger.getInstance().writeLog("Already loginned!");
				return RET_ALREADY;
			}
			
			if (output.contains(LOGIN_REQUEST_SIGNATURE)) {
				nextAction = parseAuthenPage(output);
				response = httpClient.execute(new HttpPost(nextAction));
				output = EntityUtils.toString(response.getEntity(), "GBK");
				Logger.getInstance().writeLog("Http Request:\n" + nextAction);
				Logger.getInstance().writeLog("HTTP Response:\n" + output);
				Matcher codeMatcher = loginCodePattern.matcher(output);
				if (codeMatcher.find()) {
					int code = Integer.valueOf(codeMatcher.group(1));
					if (code == 50) {
						nextAction = parseLogoutURL(output);  // prepare action parameters for logout
						Logger.getInstance().writeLog("Login success!");
					}
					Logger.getInstance().writeLog("Login code=" + code);
					return code;
				}
			} else {
				Logger.getInstance().writeLog("Can't get login page!");
			}
		} catch (Exception e) {
			Logger.getInstance().writeLog(e.toString());
			e.printStackTrace();
		}
		return RET_UNKNOWN;
	}
	
	public int logout() {
		try {
			HttpResponse response = httpClient.execute(new HttpPost(nextAction));
			String output = EntityUtils.toString(response.getEntity(), "GBK");
			Logger.getInstance().writeLog("Http Request:\n" + nextAction);
			Logger.getInstance().writeLog("HTTP Response:\n" + output);
			Matcher codeMatcher = logoutCodePattern.matcher(output);
			if (codeMatcher.find()) {
				int code = Integer.valueOf(codeMatcher.group(1));
				if (code == 150) {
					Logger.getInstance().writeLog("Logout success!");
				}
				Logger.getInstance().writeLog("Logout code=" + code);
				return code;
			}
		} catch (Exception e) {
			Logger.getInstance().writeLog(e.toString());
			e.printStackTrace();
		}
		return RET_UNKNOWN;
	}
	
	public boolean getDynamicPassword(String user, String code) {
		try {
			HttpResponse response = null;
			if (Pattern.compile("(18[09]|13[35])\\d{8}").matcher(user).find()) {
				response = httpClient.execute(new HttpGet("https://wlan.ct10000.com/getPassword.jsp?command=getpassword&phonenumber=" + user + "@" + getAccountLocation(user)));
			} else {
				response = httpClient.execute(new HttpGet("http://118.85.203.210:9010/wlan/WiFiAction.do?nowtime=0&userAccount=" + user + "&validateCode=" + code));
			}
			String output = EntityUtils.toString(response.getEntity(), "GBK");
			Logger.getInstance().writeLog("getDynamicPassword result: " + output);
			return true;
		} catch (Exception e) {
			Logger.getInstance().writeLog(e.toString());
			e.printStackTrace();
		}
		return false;
	}
	
